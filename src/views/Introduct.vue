            icon.addEventListener('click', clickIcon)
        })

        function clickIcon() {
            if ([...this.classList].indexOf('active') !== -1) {
                this.classList.remove('active')
            } else {
                icons.forEach((icon) => icon.classList.remove('active'))
                this.classList.add('active')
            }
